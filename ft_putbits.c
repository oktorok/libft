#include "libft.h"

void	ft_putbits(char src)
{
	size_t	i;

	i = 0;
	while (i < 8)
	{
		if (src & (1 << (8 - i - 1)))
			ft_putnbr(1);
		else
			ft_putnbr(0);
		i++;
	}
}
